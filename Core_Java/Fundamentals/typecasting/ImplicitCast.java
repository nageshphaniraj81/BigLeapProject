package typecasting;

public class ImplicitCast {

	public static void main(String[] args) {

		// byte to int
		byte b =  100;
		int i = b;
		System.out.println(i);
		
		// int to double
		double l = i;
		System.out.println(l);
		
		// char to int
		char ch  = 'B';
		int x = ch;
		System.out.println(x);
		
	}

}
